    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $user_name = Auth::user()->name;
        $subject_id = Crypt::decryptString($request->subject_id);
        $chapters = Chapter::where('subject_id', '=', $subject_id)->get();
        foreach ($chapters as $key => $chapter) {
            $chapters[$key]->enc_chapid = Crypt::encryptString($chapter->id);
        }
        return view('student.chapters.index', compact('user_name','chapters'));
    }

